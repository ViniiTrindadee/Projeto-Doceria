          <div class="text-justify">
            <p id="isPasted">Prepararamos uma receita deliciosa para você fazer no cantinho da sua casa. Essa receita é bem simples de preparar e utiliza poucos ingredientes. Aqui est&aacute; uma receita simples para um bolo de copo de chocolate:</p>
            <p><br></p>
            <p><span style="font-size: 18px;"><strong>Ingredientes:</strong></span></p>
            <ul>
              <li>4 colheres de sopa de farinha de trigo</li>
              <li>2 colheres de sopa de cacau em p&oacute;</li>
              <li>2 colheres de sopa de a&ccedil;&uacute;car</li>
              <li>1/4 colher de ch&aacute; de fermento em p&oacute;</li>
              <li>1/8 colher de ch&aacute; de sal</li>
              <li>3 colheres de sopa de leite</li>
              <li>2 colheres de sopa de &oacute;leo vegetal</li>
              <li>1/4 colher de ch&aacute; de extrato de baunilha</li>
              <li>1 colher de sopa de chocolate picado (opcional)</li>
              <li>Sorvete de baunilha (opcional)</li>
            </ul>
            <p><strong><span style="font-size: 18px;">Instru&ccedil;&otilde;es:</span></strong></p>
            <ol>
              <li>
                <p>Em uma tigela pequena, misture a farinha de trigo, o cacau em p&oacute;, o a&ccedil;&uacute;car, o fermento em p&oacute; e o sal.</p>
              </li>
              <li>
                <p>Adicione o leite, o &oacute;leo vegetal e o extrato de baunilha &agrave; mistura seca. Mexa bem at&eacute; obter uma massa homog&ecirc;nea. Se desejar, adicione o chocolate picado &agrave; massa para um toque extra de sabor.</p>
              </li>
              <li>
                <p>Despeje a massa em uma x&iacute;cara grande ou em um copo de cer&acirc;mica resistente ao calor, preenchendo cerca de 2/3 do recipiente.</p>
              </li>
              <li>
                <p>Leve ao micro-ondas em pot&ecirc;ncia alta por cerca de 1 minuto e 30 segundos a 2 minutos, ou at&eacute; que o bolo esteja firme e completamente cozido no centro. O tempo de cozimento pode variar dependendo da pot&ecirc;ncia do seu micro-ondas, ent&atilde;o fique de olho no bolo enquanto ele assa.</p>
              </li>
              <li>
                <p>Retire o bolo do micro-ondas e deixe esfriar por alguns minutos.</p>
              </li>
              <li>
                <p>Se desejar, sirva o bolo de copo de chocolate com uma colher de sorvete de baunilha por cima. Voc&ecirc; tamb&eacute;m pode adicionar coberturas extras, como calda de chocolate, chantilly ou frutas.</p>
              </li>
            </ol>
            <p>Aproveite o seu bolo de copo de chocolate delicioso e f&aacute;cil de fazer!</p>
